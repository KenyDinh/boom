	public static BoomGameMapEnum getRandomMap() {
		int total = 0;
		for (BoomGameMapEnum map : BoomGameMapEnum.values()) {
			total += map.getProb();
		}
		int rand = CommonMethod.random(total);
		total = 0;
		for (BoomGameMapEnum map : BoomGameMapEnum.values()) {
			total += map.getProb();
			if (rand < total) {
				return map;
			}
		}
		return BoomGameMapEnum.DEFAULT;
	}

