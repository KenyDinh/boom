	public List<Map<String, Integer>> getSpecialItemData() {
		switch(this) {
		case MAP_09:
			BoomGameItem item = BoomGameItemService.getItemById(99);
			if (item == null) {
				return null;
			}
			Map<String, Integer> itemData = new HashMap<>();
			itemData.put("id", item.getId());
			itemData.put("img", item.getImageID());
			itemData.put("x", 17);
			itemData.put("y", 3);
			return Arrays.asList(itemData);
		default:
			break;
		}
		return null;
	}

	public static BoomGameMapEnum getRandomMap() {
		int total = 0;
		for (BoomGameMapEnum map : BoomGameMapEnum.values()) {
			total += map.getProb();
		}
		int rand = CommonMethod.random(total);
		total = 0;
		for (BoomGameMapEnum map : BoomGameMapEnum.values()) {
			total += map.getProb();
			if (rand < total) {
				return map;
			}
		}
		return BoomGameMapEnum.MAP_01;
	}
	
	private static final String BOOM_MAP_DATA_FILE = "boom_map_data.dat";
	
	public static void init(ServletContext context) {
		File dataFile = new File(context.getRealPath(String.format("/WEB-INF/data/%s", BOOM_MAP_DATA_FILE)));
		if (!dataFile.exists()) {
			return;
		}
		BufferedReader breader = null;
		try {
			breader = new BufferedReader(new InputStreamReader(new FileInputStream(dataFile), "UTF-8"));
			StringBuilder sb = new StringBuilder();
			String line = null;
			while ((line = breader.readLine()) != null) {
				sb.append(line);
			}
			BoomMapDataObject mapDataObject = JSON.decode(sb.toString(), BoomMapDataObject.class);
			if (mapDataObject.hasMapData()) {
				for (BoomGameMapEnum map : BoomGameMapEnum.values()) {
					for (MapData data : mapDataObject.getMapData()) {
						if (data.getId() == map.getId()) {
							map.collisions = data.getCollision();
							break;
						}
					}
				}
			}
		} catch (FileNotFoundException e) {
			e.printStackTrace();
		} catch (UnsupportedEncodingException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}  catch (Exception e) {
			e.printStackTrace();
		} finally {
			try {
				breader.close();
			} catch (IOException e) {
				e.printStackTrace();
			}
		}
	}
	
