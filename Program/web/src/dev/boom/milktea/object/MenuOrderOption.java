package dev.boom.milktea.object;

public class MenuOrderOption {
	
	private String name;
	private long price;

	public MenuOrderOption() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getPrice() {
		return price;
	}

	public void setPrice(long price) {
		this.price = price;
	}
	
}
